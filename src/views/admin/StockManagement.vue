<template>
  <v-container>
    <v-card class="glassmorphism-card-stock">
        <v-toolbar color="transparent">
            <v-toolbar-title class="font-weight-bold">Gestão de Estoque</v-toolbar-title>
            <v-spacer></v-spacer>
            <v-btn color="primary" @click="openNewItemDialog">
                <v-icon start>mdi-plus</v-icon>
                Novo Tecido
            </v-btn>
        </v-toolbar>

        <v-data-table
            :headers="headers"
            :items="stockItems"
            :loading="loading"
            class="elevation-0 bg-transparent"
            item-value="id"
        >
            <template v-slot:item.available_meters="{ item }">
                <v-chip color="primary" variant="tonal">
                    {{ item.available_meters }}m </v-chip>
            </template>
            <template v-slot:item.actions="{ item }">
                <v-btn icon="mdi-pencil-outline" variant="text" size="small" @click="openEditDialog(item)"></v-btn>
            </template>
            <template v-slot:loading>
                <v-skeleton-loader type="table-row@5"></v-skeleton-loader>
            </template>
             <template v-slot:no-data>
                <div class="py-8 text-center text-grey">
                    Nenhum item em estoque encontrado.
                </div>
            </template>
        </v-data-table>
    </v-card>

    <v-dialog v-model="dialog" max-width="500px" persistent>
        <v-card class="glassmorphism-card">
            <v-card-title class="dialog-header">
                <span class="text-h5">{{ dialogTitle }}</span>
            </v-card-title>
            <v-card-text class="py-4">
                <v-text-field
                    v-if="!isEditing"
                    v-model="editedItem.fabric_type"
                    label="Nome do Tecido"
                    variant="outlined"
                    autofocus
                ></v-text-field>
                <v-text-field
                    v-model.number="editedItem.quantity"
                    label="Quantidade a Adicionar/Remover"
                    type="number"
                    variant="outlined"
                    :placeholder="isEditing ? `Atual: ${editedItem.current_stock}m` : ''"
                    hint="Use valores negativos para remover do estoque"
                    persistent-hint
                ></v-text-field>
            </v-card-text>
            <v-card-actions class="dialog-footer">
                <v-spacer></v-spacer>
                <v-btn text @click="closeDialog">Cancelar</v-btn>
                <v-btn color="primary" variant="flat" @click="saveStock" :loading="isSaving">Salvar</v-btn>
            </v-card-actions>
        </v-card>
    </v-dialog>
  </v-container>
</template>

<script setup lang="ts">
import { ref, onMounted, computed } from 'vue';
import { supabase } from '@/api/supabase';

type StockItem = {
  id: string;
  fabric_type: string;
  available_meters: number;
};

const stockItems = ref<StockItem[]>([]);
const loading = ref(true);
const dialog = ref(false);
const isEditing = ref(false);
const isSaving = ref(false);

const editedItem = ref<{
    id: string | null;
    fabric_type: string;
    quantity: number | null;
    current_stock?: number;
}>({
    id: null,
    fabric_type: '',
    quantity: null,
});

const headers = [
  { title: 'Tipo de Tecido', key: 'fabric_type', sortable: true },
  { title: 'Metragem Disponível', key: 'available_meters', sortable: true },
  { title: 'Ações', key: 'actions', sortable: false, align: 'end' },
];

const dialogTitle = computed(() => isEditing.value ? `Atualizar Estoque: ${editedItem.value.fabric_type}` : 'Adicionar Novo Tecido');

const fetchStock = async () => {
  loading.value = true;
  try {
    const { data, error } = await supabase.from('stock').select('*').order('fabric_type');
    if (error) throw error;
    stockItems.value = data || [];
  } catch (err: any) {
    alert(`Erro ao buscar estoque: ${err.message}`);
  } finally {
    loading.value = false;
  }
};

const openNewItemDialog = () => {
  isEditing.value = false;
  editedItem.value = { id: null, fabric_type: '', quantity: null };
  dialog.value = true;
};

const openEditDialog = (item: StockItem) => {
  isEditing.value = true;
  editedItem.value = { id: item.id, fabric_type: item.fabric_type, quantity: null, current_stock: item.available_meters };
  dialog.value = true;
};

const closeDialog = () => {
  dialog.value = false;
};

const saveStock = async () => {
  if (isSaving.value) return;
  if (!editedItem.value.fabric_type && !isEditing.value) {
      alert("O nome do tecido é obrigatório.");
      return;
  }
  if (editedItem.value.quantity === null) {
      alert("A quantidade é obrigatória.");
      return;
  }

  isSaving.value = true;

  try {
      if (isEditing.value) {
          // *** CORREÇÃO APLICADA AQUI ***
          // Trocando a chamada para a nova função otimizada
          const { error } = await supabase.rpc('update_stock_quantity', {
              p_stock_id_to_update: editedItem.value.id,
              p_quantity_to_add: editedItem.value.quantity
          });
          if (error) throw error;

      } else {
          // A lógica de inserção permanece a mesma
          const { error } = await supabase.from('stock').insert({
              fabric_type: editedItem.value.fabric_type,
              available_meters: editedItem.value.quantity
          });
          if (error) throw error;
      }
      await fetchStock();
      closeDialog();

  } catch (err: any) {
      alert(`Erro ao salvar no estoque: ${err.message}`);
  } finally {
      isSaving.value = false;
  }
};

onMounted(() => {
  fetchStock();
});
</script>

